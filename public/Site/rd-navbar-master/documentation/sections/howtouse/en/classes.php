<h3>
    Class Map
</h3>

<p>
    RD Navbar uses some CSS classes to display either changes in the navigation panel. The full list of available classes is as follows:
</p>

<ul class="makred-list">
    <li>
        <strong>.rd-navbar</strong> - the main class for navbar creation
    </li>
    <li>
        <strong>.rd-navbar-dropdown</strong> - the class that is used to create a dropdown menu
    </li>
    <li>
        <strong>.rd-navbar-megamenu</strong> - the class that is used to create a megamenu
    </li>
    <li>
        <strong>[data-rd-navbar-toggle]</strong> - the data attribute used to create the switches
    </li>
    <li>
        <strong>.rd-navbar-submenu</strong> - the class that is added to the parent element .rd-navbar-dropdown, .rd-navbar-megamenu
    </li>
    <li>
        <strong>.rd-navbar-submenu-toggle</strong> - the class that is used to create the switch of the submenu.
    </li>
    <li>
        <strong>.rd-navbar--has-dropdown</strong> - the class that is added to the parent element .rd-navbar-dropdown
    </li>
    <li>
        <strong>.rd-navbar--has-megamenu</strong> - the class that is added to the parent element .rd-navbar-megamenu
    </li>
    <li>
        <strong>.focus</strong> - the class that is added to the .rd-navbar-submenu when the mouse is clicked on the hover
    </li>
    <li>
        <strong>.opened</strong> - the class that is added to the .rd-navbar-submenu when switching the display of the submenu
    </li>
    <li>
        <strong>.rd-navbar--is-clone</strong> - the class that is added to the clone-element of navbar
    </li>
    <li>
        <strong>.rd-navbar--is-stuck</strong> - the class that is added when sticking of the Navbar to the original element or clone-element, depending on the settings.
    </li>
</ul>

<p>
    All classes that are not listed in the list above are exclusively optional and do not affect the operation of the RD Navbar.
</p>