<h3>
    Карта классов
</h3>

<p>
    RD Navbar использует некоторые css классы для отображения тех или инных изменений в навигационной панели.
    Полный список доступных классов представлен ниже:
</p>

<ul class="makred-list">
    <li>
        <strong>.rd-navbar</strong> - основной класс для создания навбара
    </li>
    <li>
        <strong>.rd-navbar-dropdown</strong> - класс, который используется для создания выпадающего меню
    </li>
    <li>
        <strong>.rd-navbar-megamenu</strong> - класс, который используется для создания мегаменю
    </li>
    <li>
        <strong>[data-rd-navbar-toggle]</strong> - дата-атрибут, который используется для создания переключателей
    </li>
    <li>
        <strong>.rd-navbar-submenu</strong> - класс, который добавляется на родительский элемент .rd-navbar-dropdown, .rd-navbar-megamenu
    </li>
    <li>
        <strong>.rd-navbar-submenu-toggle</strong> - класс, который используется для создания переключателя подменю.
    </li>
    <li>
        <strong>.rd-navbar--has-dropdown</strong> - класс, который добавляется на родительский элемент .rd-navbar-dropdown
    </li>
    <li>
        <strong>.rd-navbar--has-megamenu</strong> - класс, который добавляется на родительский элемент .rd-navbar-megamenu
    </li>
    <li>
        <strong>.focus</strong> - класс, который добавляется на .rd-navbar-submenu при ховере мышью
    </li>
    <li>
        <strong>.opened</strong> - класс, который добавляется на .rd-navbar-submenu при переключении отображения субменю
    </li>
    <li>
        <strong>.rd-navbar--is-clone</strong> - класс, который добавляется на клон-элемент навбара
    </li>
    <li>
        <strong>.rd-navbar--is-stuck</strong> - класс, который добавляется при прилипании навбара на оригинальный элемент или
        клон-элемент, в зависимости от настроек.
    </li>
</ul>

<p>
    Все классы, которые не указаны в списке выше являются сугубо опциональными и никак не влияют на работу RD Navbar.
</p>